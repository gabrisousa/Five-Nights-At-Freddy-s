function ajustarVideoIntro() {
    let video = document.getElementById("introVideo");
    
    if (window.innerWidth < 768) {
      video.src = "./src/videos/video-intro-responsivo.mp4";
    } else {
      video.src = "./src/videos/video-intro.mp4";
    }
  }


  window.addEventListener("resize", ajustarVideoIntro);
window.addEventListener("load", ajustarVideoIntro);




